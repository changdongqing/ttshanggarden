using TTShang.Client.Auto.Entry.Components;
using TTShang.Core.Client.Authorization;
using Microsoft.Extensions.DependencyInjection.Extensions;

namespace TTShang.Client.Auto.Entry
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddRazorComponents()
                .AddInteractiveServerComponents()
                .AddInteractiveWebAssemblyComponents();
            builder.Services.AddHttpContextAccessor();

            builder.Services.AddAuthentication();

            builder.Services.TryAddScoped<ILoginDataAccessor, ServerLoginDataAccessor>();

            TTShang.Client.Entry.ServiceCombine.AddServices(builder.Services, builder.Configuration);



            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseWebAssemblyDebugging();
                app.UseMigrationsEndPoint();
            }
            else
            {
                app.UseExceptionHandler("/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();

            app.UseStaticFiles();
            app.UseAntiforgery();

            app.MapRazorComponents<App>()
                .AddInteractiveServerRenderMode()
                .AddInteractiveWebAssemblyRenderMode()
                .AddAdditionalAssemblies(TTShang.Core.Client.Impl.Extensions.ModuleExtension.AllModeuleAssemblies);
            app.Run();
        }
    }
}
